import numpy as np

# Функция, которая заменяет каждое значение массива на количество его появлений в массиве
def replace_with_counts(arr):
    # bincount считает, сколько раз встречается каждое число,
    # при этом индексы массива = сами значения
    counts = np.bincount(arr)

    # возвращаем новый массив, где каждое значение заменено на количество
    # приводим к обычному int, чтобы не было np.int64 в выводе
    return [int(counts[x]) for x in arr]


# простые тесты, чтобы показать работу функции на разных входах
def run_tests():
    test_arrays = [
        [1, 2, 1, 5, 1, 2],
        [5, 5, 5, 5],           # все одинаковые
        [1, 2, 3, 4],           # все разные
        [10, 10, 20, 30, 20, 10],
        [0, 0, 0, 1, 1]
    ]

    print("\nТесты:")
    for arr in test_arrays:
        print(f"{arr} -> {replace_with_counts(arr)}")


# Основная программа
def main():
    print("Введите массив из неотрицательных чисел через пробел:")
    # читаем ввод и превращаем в список чисел
    arr = list(map(int, input().split()))

    # заменяем значения на количество повторений
    result = replace_with_counts(arr)

    print("Результат:")
    print(result)

    # запускаем тесты для проверки
    run_tests()


main()
